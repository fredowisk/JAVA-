    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        try {
            if (Media() == 0){
                JOptionPane.showMessageDialog(null, "Lembre-se de pressionar ENTER após"
                        + " digitar as notas!");
            }
            else if (NP1 < 0 || NP2 < 0) {
                JOptionPane.showMessageDialog(null, "Valores inválidos!");
            } else if (Media() > 10) {
                JOptionPane.showMessageDialog(null, "As notas ultrapassaram o limite!");
            } else {
            Media();
            txtMedia.setText(String.valueOf(Media()));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro na execução do calculo! Tente novamente.");
        }
    }//GEN-LAST:event_btnCalcularActionPerformed

