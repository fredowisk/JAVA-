    private void btnSituacaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSituacaoActionPerformed
        // TODO add your handling code here:
        try {
        if (Media() == 0){
                JOptionPane.showMessageDialog(null, "Lembre-se de pressionar ENTER após"
                        + " digitar as notas!");
        }
        else if (Media() > 7) {
            txtSituacao.setText("Aprovado!");
        } else {
            txtSituacao.setText("Reprovado!");
        }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro na execução");
        }

    }//GEN-LAST:event_btnSituacaoActionPerformed

    public double Media() {
        return (NP1 + NP2) / 2;
    }

